import java.util.Scanner;
class OddEven1
{
  public static void main(String args[])
  {
    System.out.println("To print odd numbers");
    int i;
    for(i=1;i<=10;i=i+2)
    System.out.println(i);
    System.out.println("To print even numbers");
    int j;
    for(j=2;j<=10;j=j+2)
    System.out.println(j);
  }
}